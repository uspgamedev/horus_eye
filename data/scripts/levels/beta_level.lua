function setup ()
  print "Setting level up..."
  for i = 2,3 do
    for j = 1,3 do
      local door = context.WorldObjectByTag("DOOR-"..i.."-"..j)
      if door then
        local damageable = component.Damageable(door)
        damageable:set_life(component.Energy(2))
        door:set_damageable(damageable)
      end
    end
  end
end
